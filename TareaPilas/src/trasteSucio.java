public class trasteSucio {
    
    private String numerotrasteSucio;
    private int vasos;
    private int cubiertos;

    public trasteSucio(String numerotrasteSucio, int vasos, int cubiertos) {
        this.numerotrasteSucio = numerotrasteSucio;
        this.vasos = vasos;
        this.cubiertos = cubiertos;
    }

    public String getNumerotrasteSucio() {
        return numerotrasteSucio;
    }

    public int getVasos() {
        return vasos;
    }

    public int getCubiertos() {
        return cubiertos;
    }
    
    
    
}
